function getAge(birthDateStr, referenceDate){
  if (!birthDateStr) return null;
  const bd = new Date(birthDateStr);
  if (isNaN(bd)) return null;
  const ref = referenceDate || new Date();
  let age = ref.getFullYear() - bd.getFullYear();
  const m = ref.getMonth() - bd.getMonth();
  if (m < 0 || (m === 0 && ref.getDate() < bd.getDate())) age--;
  return age;
}

function getMilitaryCategory(person, referenceYear){
  if (person.calculationMode === 'manual' && person.manualCategory) return person.manualCategory;
  const branch = (person.serviceBranch||'').toLowerCase();
  const status = (person.status||'').toLowerCase();
  const referenceDate = referenceYear ? new Date(referenceYear,11,31) : new Date();
  const age = getAge(person.birthDate, referenceDate);
  const dischargeDate = person.dischargeDate ? new Date(person.dischargeDate) : null;
  if (!dischargeDate || isNaN(dischargeDate.getTime())){
    if (/민방위|civil/i.test(branch) || /민방위|civil/i.test(status)) return '민방위';
    return '대상아님';
  }
  if (dischargeDate > referenceDate) return '대상아님';
  const serviceYear = referenceDate.getFullYear() - dischargeDate.getFullYear() + 1;
  if (serviceYear >=1 && serviceYear <=8) return '예비군';
  if (serviceYear >8){
    if (age !== null && age >=19 && age <=59) return '민방위';
    return '대상아님';
  }
  if (/민방위|civil/i.test(branch) || /민방위|civil/i.test(status)) return '민방위';
  return '대상아님';
}

function getTrainingYear(person, referenceYear){
  if (person.calculationMode === 'manual' && person.manualYear) return Number(person.manualYear)||0;
  if (!person.dischargeDate) return 0;
  const dischargeDate = new Date(person.dischargeDate);
  if (isNaN(dischargeDate.getTime())) return 0;
  const effectiveYear = referenceYear ?? new Date().getFullYear();
  return effectiveYear - dischargeDate.getFullYear() + 1;
}

function computeAutoGenerated(person, rules, referenceYear, autoConfig){
  const results = [];
  // check shouldAutoCreate
  if (!autoConfig.enabled) return results;
  if (!person.status) return results;
  if (!autoConfig.targetStatuses || autoConfig.targetStatuses.length===0) return results;
  if (!autoConfig.targetStatuses.includes(person.status)) return results;
  // excluded statuses
  if (['퇴사','전출','휴직'].some(s => person.status.includes(s))) return results;

  const personYear = getTrainingYear(person, referenceYear);
  const applicable = rules.filter((r)=>{
    try{
      if (r.currentCategory && r.currentCategory !== getMilitaryCategory(person, referenceYear)) return false;
      if (r.yearMin != null && personYear < Number(r.yearMin)) return false;
      if (r.yearMax != null && personYear > Number(r.yearMax)) return false;
      if (r.mobilizationOnly && !person.mobilization) return false;
      if (!r.enabled) return false;
      return true;
    }catch(e){return false;}
  });
  let selectedRules = applicable;
  if (applicable.length >1){
    const hasMobilized = applicable.some(x=>!!x.mobilizationOnly);
    const hasNon = applicable.some(x=>!x.mobilizationOnly);
    if (hasMobilized && hasNon){
      selectedRules = person.mobilization ? applicable.filter(x=>!!x.mobilizationOnly) : applicable.filter(x=>!x.mobilizationOnly);
    }
  }
  selectedRules.forEach(r=>{
    results.push({
      trainingType: r.trainingType||r.name||'훈련',
      trainingRound: r.trainingRound||'1차',
      trainingYear: r.year||new Date().getFullYear().toString(),
      trainingHours: Number(r.requiredHours)||0,
      notes: '자동생성',
      status: '일정미등록'
    });
  });
  return results;
}

